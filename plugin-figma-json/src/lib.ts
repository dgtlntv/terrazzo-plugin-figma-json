/**
 * Token types supported by Figma.
 */
export const SUPPORTED_TYPES = [
  'color',
  'dimension',
  'duration',
  'fontFamily',
  'fontWeight',
  'number',
  'typography',
] as const;

export type SupportedType = (typeof SUPPORTED_TYPES)[number];

/**
 * Token types that are not supported by Figma and will be dropped with a warning.
 */
export const UNSUPPORTED_TYPES = [
  'shadow',
  'border',
  'gradient',
  'transition',
  'strokeStyle',
  'cubicBezier',
] as const;

export type UnsupportedType = (typeof UNSUPPORTED_TYPES)[number];

/**
 * Color spaces that Figma natively supports.
 */
export const FIGMA_COLOR_SPACES = ['srgb', 'hsl'] as const;

export type FigmaColorSpace = (typeof FIGMA_COLOR_SPACES)[number];

/**
 * Figma variable types.
 */
export type FigmaVariableType = 'Color' | 'Number' | 'String' | 'Boolean';

/**
 * Extension keys used by Figma in the $extensions object.
 * Figma uses flat namespaced keys like "com.figma.type", not nested objects.
 */
export interface FigmaExtensionKeys {
  /**
   * Overrides the inferred Figma variable type.
   * Use "boolean" to treat a number token as a boolean (0 = false, non-zero = true).
   * Key: "com.figma.type"
   */
  'com.figma.type'?: 'boolean';
  /**
   * Cross-collection alias data for Figma.
   * Key: "com.figma.aliasData"
   */
  'com.figma.aliasData'?: {
    collection: string;
    mode: string;
  };
}

/**
 * Token extensions object that may include Figma-specific keys.
 */
export type TokenExtensions = FigmaExtensionKeys & {
  [key: string]: unknown;
};

/**
 * Output token structure for Figma-compatible JSON.
 */
export interface FigmaTokenValue {
  /**
   * The DTCG token type.
   */
  $type: string;
  /**
   * The Figma-compatible value.
   */
  $value: unknown;
  /**
   * Optional description.
   */
  $description?: string;
  /**
   * Optional extensions, including Figma-specific ones.
   */
  $extensions?: TokenExtensions;
}

/**
 * Mapping from DTCG types to Figma variable types.
 * Note: Typography is a composite type that splits into sub-tokens, so it doesn't
 * have a direct Figma mapping. The placeholder 'String' is never actually used.
 */
export const DTCG_TO_FIGMA_TYPE_MAP: Record<SupportedType, FigmaVariableType> = {
  color: 'Color',
  dimension: 'Number',
  duration: 'Number',
  fontFamily: 'String',
  fontWeight: 'Number', // Can also be String depending on input value
  number: 'Number', // Can also be Boolean via extension
  typography: 'String', // Composite type - splits into sub-tokens, this is never used
};

/**
 * Context passed to converters.
 */
export interface ConverterContext {
  logger: Logger;
  options: FigmaJsonPluginOptions;
  tokenId: string;
  /**
   * Token extensions, if any. Used to check for Figma-specific type overrides.
   */
  extensions?: TokenExtensions;
  /**
   * All tokens in the design system, used for alias validation.
   */
  allTokens?: Record<string, TokenNormalized>;
  /**
   * Original value before resolution (for checking references in composite types).
   */
  originalValue?: unknown;
  /**
   * Partial alias information for composite types (e.g., typography).
   * Maps sub-property names to their alias target token IDs.
   */
  partialAliasOf?: Record<string, string | undefined>;
}

/**
 * Sub-token information for split composite tokens (e.g., typography).
 */
export interface SubToken {
  /** Suffix to append to parent token ID (e.g., "fontFamily" -> parent.id + ".fontFamily") */
  idSuffix: string;
  /** The $type for this sub-token */
  $type: string;
  /** The converted value */
  value: unknown;
  /** If this sub-property was a reference, the target token ID */
  aliasOf?: string;
}

/**
 * Result returned by a converter.
 */
export interface ConverterResult {
  /** The converted value, or undefined if the token should be skipped. */
  value: unknown;
  /** Whether to skip this token (not include in output). */
  skip?: boolean;
  /** Override the output $type (e.g., fontWeight outputs as 'number' or 'string'). */
  outputType?: string;
  /** Whether this is a split composite token (e.g., typography). */
  split?: boolean;
  /** Sub-tokens for split composite types. */
  subTokens?: SubToken[];
}

